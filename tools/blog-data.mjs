export function getBlogPost(slug, lang) {
  const row = DATA[slug];
  if (!row) {
    return lang === "ar" ? fallbackPostAr(slug) : fallbackPostEn(slug);
  }
  const pack = row[lang] || row.en;
  return pack;
}

export { SLUGS };

for (const s of SLUGS) {
  if (!DATA[s]) DATA[s] = {};
  if (!DATA[s].en) DATA[s].en = fallbackPostEn(s);
  if (!DATA[s].ar) DATA[s].ar = fallbackPostAr(s);
}
